#include "libft.h"

void	*ft_bzero(void *mass, size_t size)
{
	size_t	n;

	n = size;
	while (n != 0)
	{
		n--;
		((char *)mass)[n] = '\0';
	}
	if (n == 0)
	{
		if (size != 0)
			((char *)mass)[n] = '\0';
	}
	return (mass);
}
